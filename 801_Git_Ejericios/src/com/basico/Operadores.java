package com.basico;

public class Operadores {

	public static void main(String[] args) {
		
		int n1 = 100;
		int n2 = 50;
		System.out.println("la suma es " +(n1+n2));
		System.out.println("la resta es "+(n1-n2));
		System.out.println("la * es "+(n1*n2));
		System.out.println("la / es "+(n1/n2));
		System.out.println("el % es "+(n1%n2));
	}

}
